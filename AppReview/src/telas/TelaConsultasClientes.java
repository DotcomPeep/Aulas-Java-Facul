    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Limpar();  
        if (cmbTiposConsultas.getSelectedItem().toString().equals("Consulta por cidade")){
            lblCidade.setVisible(true);
            txtCidade.setVisible(true);
        } else if (cmbTiposConsultas.getSelectedItem().toString().equals("Consulta por renda")){ 
           lblMinimo.setVisible(true);
           txtMinimo.setVisible(true);
           lblMaximo.setVisible(true);
           txtMaximo.setVisible(true);
        }else {
           lblCidade.setVisible(false);
           txtCidade.setVisible(false);
           lblMinimo.setVisible(false);
           txtMinimo.setVisible(false);
           lblMaximo.setVisible(false);
           txtMaximo.setVisible(false);
        }
        try {
            //1) Conectar no BD
            Connection con;
            PreparedStatement st;
            ResultSet rs;
            String combo;
            DefaultTableModel tblClientes;
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/acesso", "root", "987543@#Leo");
            
            //2) Fazer a busca do cpf digitado no campo
            //txtCpf, na tabela cliente
            combo = cmbTiposConsultas.getSelectedItem().toString();
            if(combo.equalsIgnoreCase("Consulta geral de clientes")) {
                st = con.prepareStatement("SELECT * FROM cliente");
                rs = st.executeQuery();
                tblClientes = (DefaultTableModel) tblConsultas.getModel();
                while(rs.next()) { //Enquanto tiver linha de cliente no rs
                    Object[] dados = {rs.getString("nome"), rs.getString("cpf"), rs.getString("endereco"), rs.getString("telefone")};
                    tblClientes.addRow(dados);
                }
            }else if(combo.equalsIgnoreCase("Consulta por cidade")) {
                st = con.prepareStatement("SELECT * FROM cliente WHERE cidade = ?");
                st.setString(1, txtCidade.getText());
                rs = st.executeQuery();
                tblClientes = (DefaultTableModel) tblConsultas.getModel();
                while(rs.next()) { //Enquanto tiver linha de cliente no rs
                    Object[] dados = {rs.getString("nome"), rs.getString("cpf"), rs.getString("endereco"), rs.getString("telefone")};
                    tblClientes.addRow(dados);
                }
            } else if (combo.equalsIgnoreCase("Consulta por renda")) {
                st = con.prepareStatement("SELECT * FROM cliente WHERE renda BETWEEN ? AND ?");
                st.setDouble(1, Double.parseDouble(txtMinimo.getText()));
                st.setDouble(2, Double.parseDouble(txtMaximo.getText()));
                rs = st.executeQuery();
                tblClientes = (DefaultTableModel) tblConsultas.getModel();
                while (rs.next()) { // Enquanto tiver linnha de cliente no rs
                    Object[] dados = {
                        rs.getString("cpf"),
                        rs.getString("nome"),
                        rs.getString("telefone"),
                        rs.getString("renda")};
                    tblClientes.addRow(dados);
                }

            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar acessar a base de dados, entre em contato com o administrador e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

