    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            //1) Conectar no BD
            Connection con;
            PreparedStatement st;
            ResultSet rs;
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/acesso", "root", "987543@#Leo");
            
            //2) Fazer a busca do cpf digitado no campo
            //txtCpf, na tabela cliente
            st = con.prepareStatement("SELECT * FROM cliente WHERE cpf = ?");
            st.setString(1, txtCpf.getText());
            
            rs = st.executeQuery(); //Executa o SELECT na tabela do BD
            if(rs.next()) { //se encontrou o cpf no BD
                txtNome.setText(rs.getString("nome"));
                txtEndereco.setText(rs.getString("endereco"));
                txtBairro.setText(rs.getString("bairro"));
                txtCidade.setText(rs.getString("cidade"));
                txtTelefone.setText(rs.getString("telefone"));
                txtRenda.setText(rs.getString("renda"));
                btnExcluir.setEnabled(true);
                btnAlterar.setEnabled(true);
                txtCpf.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null, "CPF não encontrado");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar acessar a base de dados, entre em contato com o administrador e informe o erro: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_btnConsultarActionPerformed

