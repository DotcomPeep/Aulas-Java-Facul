    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // Conectar no BD
        try {
            Connection con;
            PreparedStatement st;
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/acesso", "root", "987543@#Leo");
        
        // Executar a alteração(Update)
        st = con.prepareStatement("UPDATE cliente SET nome = ?, endereco = ?, bairro = ?, cidade = ?, telefone = ?, renda = ? WHERE cpf = ?");
        st.setString(1, txtNome.getText());
        st.setString(2, txtEndereco.getText());
        st.setString(3, txtBairro.getText());
        st.setString(4, txtCidade.getText());
        st.setString(5, txtTelefone.getText());
        st.setDouble(6, Double.parseDouble(txtRenda.getText()));
        st.setString(7, txtCpf.getText());
        st.executeUpdate();
        
        // Dar um feedback para o usuário
        JOptionPane.showMessageDialog(null, "Dados alterados com sucesso");
        Limpar();
        txtCpf.setEditable(true);
        btnExcluir.setEnabled(false);
        btnAlterar.setEnabled(false);
         } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar alterar os dados, entre em contato com o administrador e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

